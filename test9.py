#Logical Operator.



x = (15 > 10) # output: True
y = (15 < 10) # output: False
z = (10 < 15) # output: True



#Logical Operator: and
print(x and y) # output: False

print(x and z) # output: True

print(x and y and z) # output: False



#Logical Operator: or
print(x or y) # output: True

print(x or z) # output: True

print(x or z or y) # output: True


#Logical Operator: not

x = (15 > 10) # output: True
y = (15 < 10) # output: False

print(not x) # output: False

print(not y) # output: True